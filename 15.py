#输入一个 int 型的正整数，计算出该 int 型数据在内存中存储时 1 的个数。(二进制)

"""
a=60,b=13,a=0011 1100,b=0000 1101
&是按位与运算符:a&b的意思是a的二进制和b的二进制比较,两个对应位都为1则为1,否则为0,a&b=0000 1100=12
|是按位或运算符:只要两个对应位有一个为1时,结果为就为1,a|b=0011 1101=61
^是按位异或运算符:只要两对应的二进位相异时,结果为1,a^b=0011 0001=49
<<左移运算符:运算数的各二进位全部左移若干位,由<<右边的数字指定移动的位数,高位丢弃,低位补0, a<<2=1111 0000=240
>>右移运算符:同上,a>>2=0000 1111=15
"""

n=int(input())
count=0
while n>0:
    count+=n&1
    n=n>>1
print(count)